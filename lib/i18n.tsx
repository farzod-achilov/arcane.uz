// ─── Context ──────────────────────────────────────────────────────────────────

// eslint-disable-next-line @typescript-eslint/no-explicit-any
type DeepString<T> = T extends string ? string : { [K in keyof T]: DeepString<T[K]> };
type AnyTranslations = DeepString<Translations>;

interface LangCtx {
  lang:    Lang;
  setLang: (l: Lang) => void;
  t:       AnyTranslations;
}

import React from 'react';
const LangContext = React.createContext<LangCtx>({
  lang:    'ru',
  setLang: () => {},
  t:       TRANSLATIONS.ru as AnyTranslations,
});

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('ru');

  useEffect(() => {
    const stored = localStorage.getItem('arcane-admin-lang') as Lang | null;
    if (stored === 'en' || stored === 'ru') setLangState(stored);
  }, []);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    localStorage.setItem('arcane-admin-lang', l);
  }, []);

  return (
    <LangContext.Provider value={{ lang, setLang, t: TRANSLATIONS[lang] as AnyTranslations }}>
      {children}
    </LangContext.Provider>
  );
}

export function useT() {
  return useContext(LangContext);
}
